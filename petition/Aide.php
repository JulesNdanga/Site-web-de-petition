<div id="h3"><h3>Aide</h3></div>

<p class="paragr2">
Bienvenue sur l'aide de NJ&MP Dans cette rubrique vous verrez comment utiliser notre site afin de rechercher et de signer les pétitions que vous cautionnez, de même, vous serez capable de créer votre propre pétition en ligne.
</p>

<p class="paragr2">
<h1>Rechercher une pétition</h1>
Sur la page principale vous pouvez trouver une pétition grâce au moteur de recherche en haut à droite à l'aide du champ de recherche en tapant les mots-clefs de la pétition recherchée.
<br>
Vous arriverez alors sur une liste paginée de toutes les pétitions correspondantes à votre recherche.
En cliquant sur la pétition souhaitée, vous arriverez sur la page destinée à cette pétition avec la possibilité de la signer.
</p>

<p class="paragr2">
<h1>Signer une pétition</h1>
Pour signer une pétition, il suffit d'aller en bas de la pétition, d'inscrire votre adresse e-mail et de cliquer sur signer avec votre mail.
Attention de remplir correctement les différents champs du formulaire, notamment l'adresse mail car elle vous permettra de valider votre signature. Ce formulaire n'est à remplir que lors de votre première visite sur notre site. Vos informations restent en mémoire.
Suite à votre validation vous recevrez un mail avec un lien vous demandant de confirmer votre signature, ainsi, personne ne peut signer une pétition en votre nom !
</p>

<p class="paragr2">
<h1>Modifier une pétition</h1>
Pour modifier une pétition dont vous êtes l'auteur, logguez-vous sur votre compte puis cliquez sur
modifier ma pétition
Vous pourrez alors changer votre texte, votre date, votre destinataire.
</p>

<p class="paragr2">
<h1>Imprimer une pétition</h1>
Pour imprimer une pétition, logguez-vous puis cliquez sur mes signatures en PDF.
Vous pouvez alors imprimer votre pétition et ses signatures.
</p>
<p class="paragr2">
<h1>Pourquoi créer un compte sur NJ&MP</h1>
Tout simplement pour pouvoir créer votre pétition (et non pour signer une pétition). Remplissez le formulaire en choissant un identifiant et un mot de passe qui serviront par la suite à vous identifier sur le site via la page d'accueil, puis validez.  Suite à votre validation vous recevrez un mail avec un lien vous demandant de confirmer votre inscription.
</p>

<p class="paragr2">
<h1>Créer une pétition</h1>
Ce lien n'apparaît que si vous vous êtes identifié au préalable.
Lisez scrupuleusement puis validez la page A lire avant de créér une pétition.
</p>
